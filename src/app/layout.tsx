"use client"; // 추가: onLoad는 클라이언트에서 실행되므로 use client 필요

import { useState } from "react";
import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";
import Script from 'next/script';
import { ScriptLoadProvider, useScriptLoad } from "@/context/ScriptLoadContext";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

// Layout 컴포넌트를 분리하여 클라이언트 컴포넌트와 서버 컴포넌트를 함께 사용
function LayoutContent({ children }: { children: React.ReactNode }) {
  const { setIsLoaded } = useScriptLoad();
  return (
    <body
      className={`${geistSans.variable} ${geistMono.variable} antialiased`}
    >
      {children}
      <Script
        strategy="beforeInteractive"
        src={`https://oapi.map.naver.com/openapi/v3/maps.js?ncpClientId=${process.env.NEXT_PUBLIC_NAVER_CLIENT_ID}`}
        onLoad={() => {
          setIsLoaded(true);
        }}
      />
    </body>
  );
}


export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en">
      <ScriptLoadProvider>
        {/* LayoutContent에 children을 전달 */}
        <LayoutContent>{children}</LayoutContent>
      </ScriptLoadProvider>
    </html>
  );
}
